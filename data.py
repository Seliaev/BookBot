def get_book_by_id(book_id: str) -> dict | None:
    for books in BOOKS.values():
        for book in books:
            if book["id"] == book_id:
                return book
    return None


def search_books(query: str) -> list[dict]:
    query = query.lower()
    results = []
    for books in BOOKS.values():
        for book in books:
            if (query in book["title"].lower() or
                    query in book["author"].lower()):
                results.append(book)
    return results


import random

def get_random_book() -> dict:
    all_books = [b for books in BOOKS.values() for b in books]
    return random.choice(all_books)
